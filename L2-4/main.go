package main

import "fmt"

// Что выведет программа?

// Объяснить вывод программы.

func main() {
	ch := make(chan int)
	go func() {
		for i := 0; i < 10; i++ {
			ch <- i
		}
		close(ch)
	}()
	for n := range ch {
		fmt.Println(n)
	}
}


// объяснение:
// Так как у нас небуферизованный (блокирующийся) канал, который может 
// передавать только одно значение за раз, то в данном примере горутина 
// будет записывать в цикле значения в канал и ждать, пока главная 
// горутина не получит его, и так до конца цикла.
// 
// Однако программа завершится с ошибкой deadlock, потому что:
// 1. Горутина отправит все значения от 0 до 9 и завершится
// 2. Канал останется открытым (не вызван close(ch))
// 3. Оператор range будет ждать новых значений из канала бесконечно
// 4. Все горутины окажутся заблокированными - deadlock!